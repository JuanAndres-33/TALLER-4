    static final String[] COLOR = {
            "\u001B[47m",
            "\u001B[41m", 
            "\u001B[42m", 
            "\u001B[44m", 
            "\u001B[43m", 
            "\u001B[40m", 
            "\u001B[48;5;208m"
    };
