    static void dibujar(int[][] f){
        for(int i=0;i<3;i++){
            for(int j=0;j<9;j++){
                System.out.print(COLOR[f[i][j]] + "  " + RESET);
            }
            System.out.println();
        }
        System.out.println();
    }

